const mongoose = require("mongoose");

mongoose.set("strictQuery", true);
const ContactSchema = mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "users",
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
  },
  type: {
    type: String,
    default: "personal",
  },
  date: {
    type: Date,
    default: Date.now,
  },
  profileimg: {
    type: String,
    default: null,
  },
});

module.exports = mongoose.model("contact", ContactSchema);
